function remove(){
    if(document.getElementById('pimg').style.visibility="visible"){
        document.getElementById('pimg').style.visibility="hidden";
        document.getElementById('rmv').style.visibility="hidden";
    }
    if(document.getElementById('uimg').style.visibility="visible"){
        document.getElementById('uimg').style.visibility="hidden";
        document.getElementById('rmv').style.visibility="hidden";
    }
    
}